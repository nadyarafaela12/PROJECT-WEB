<b>VISI DAN MISI</b>
<p><b>Visi </b> <br>
Terwujudnya Sekolah yang RELIGIUS, HUMANIS DAN BERKEMAJUAN.
 </p> 

<p>
	<b>Misi</b>
<p>1)	Melaksanakan Pendidikan akhlak karimah dalam kehidupan sehari-hari.</p>
<p>2)	Meningkatakan kualitas guru dan karyawan dalam rangka peningkatan profesi, prestasi dan produktifitas.</p>
<p>3)	Mewujudkan lingkungan madrasah yang bersih, asri, nyaman dan Islami.</p>
<p>4) 	Mengembangkan potensi peserta didik baik akademik maupun  non akademik sesuai dengan bakat dan mintanya melalui proses pembelajaran yang berkualitas.</p>
